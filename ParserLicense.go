//MarshalYAML is used to serialise a LicenseContainer
func (t LicenseContainer) MarshalYAML() (interface{}, error) {
	for _, license := range t.Data {
		if !LicenseExists(string(license)) {
			return nil, errors.New("invalid license: " + string(license))
		}
	}

	return t.Data, nil
}

//UnmarshalYAML creates a LicenseContainer from a license string or array
func (t *LicenseContainer) UnmarshalYAML(v *yaml.Node) error {
	if v.Kind == yaml.ScalarNode && LicenseExists(v.Value) {
		t.Data = []License{
			License(v.Value),
		}
	} else {
		var licenseArray []License
		err := v.Decode(&licenseArray)
		if err != nil {
			return err
		}
		for _, license := range licenseArray {
			if !LicenseExists(string(license)) {
				return errors.New("License " + string(license) + " does not exist")
			}
		}
		t.Data = licenseArray
	}

	return nil
}

//MakeLicenses creates a License array from a Licenses
func MakeLicenses(license ...License) LicenseContainer {
	return LicenseContainer{Data: license}
}

//MakeLicensesByString creates a License array from strings
func MakeLicensesByString(license ...string) LicenseContainer {
	var licenses = make([]License, len(license))
	for i, v := range license {
		licenses[i] = License(v)
	}
	return LicenseContainer{Data: licenses}
}

